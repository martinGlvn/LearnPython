# Operadores =>
print(3+3)  # 6
print(6-3)  # 3
print(10*5)  # 50
print(20/2)  # 10
print(10 % 2)  # 0
print(10//3)  # 3 -> Division aproximada
print(3**3)  # 27 -> Elevar n^

# Concatenacion =>
print("hola"+" como"+" estas?")

# Operadores de Comparacion => True or False
print(3 > 4)
print(3 >= 4)
print(3 < 4)
print(3 <= 4)
print(3 == 4)
print(3 != 4)
print("hola" > "hi")  # True -> compara la cantidad de letras.

# Operadores Logicos =>
print(3 > 2 and "hola" < "hi")  # y -> False
print(5 > 3 or 6 < 3)  # o -> True
print(not (3 > 10))  # negacion -> True
