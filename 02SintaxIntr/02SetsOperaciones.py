set_a = {'chile', 'uruguay', 'brasil'}
set_b = {'argentina', 'brasil', 'colombia'}

# Union de Conjuntos ->
set_c = set_a.union(set_b)
print(set_c)  # {'argentina', 'uruguay', 'colombia', 'chile', 'brasil'
set_test = (set_a | set_b)
print(set_test)  # {'colombia', 'uruguay', 'argentina', 'chile', 'brasil'}

