# Datos flotantes =>
myValue = 3.14
print(myValue)
print(type(myValue))  # 3.14
print(str(myValue) + " is of the data type " + str(type(myValue)))

# Datos complejos =>
myValue2 = 5j
print(myValue2)  # 5j
print(type(myValue2))  # complex
print(str(myValue2) + " is of the data type " + str(type(myValue2)))

# Datos Booleanos =>
myValue3 = True
print(myValue3)
print(type(myValue3))  # Bool
print(str(myValue3) + " is of the data type " + str(type(myValue3)))

myValue4 = False
print(myValue4)
print(type(myValue4))  # Bool
print(str(myValue4) + " is of the data type " + str(type(myValue4)))
